
public class ShorthandOperator {

    public static void main(String[] args) {
        int a = 5;
        a += 1;  // 6
        System.out.println(a);
        a -= 1;
                  // -6
        System.out.println(a);
        a *= 1;
        System.out.println(a);
        a /= 1;

        System.out.println(a);
        a %= 1;
        System.out.println(a);

    }
}
